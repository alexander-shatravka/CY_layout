                                </p>
                                <p>&nbsp;</p>
                                <p><span style="font-weight: 400;">Підводячи підсумок, можна стверджувати, що банки можуть списувати борги повністю або частково. Але для позичальника процес списання має не завжди позитивні наслідки. Тому, перед тим, як розраховувати на списання позики, краще зв'язатися з кредитною установою і спробувати з нею домовитися. Як правило, банки йдуть назустріч своїм клієнтам, оскільки зацікавлені у виплатах.</span>
